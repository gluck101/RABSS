// Function to check if orders are allowed based on time
function isOrderAllowed() {
    // Get the current time
    const currentTime = new Date();
    // Get the current hour in UTC (Coordinated Universal Time)
    const currentHour = currentTime.getUTCHours() + 0; // Adjust for Ghana time (+0 for UTC)
    // Get the current minute in UTC
    const currentMinute = currentTime.getUTCMinutes();

    // Define the time range for accepting orders (4:00 AM to 12:00 PM).
    const openingTimeHour = 3; // Opening Time in Hour
    const openingTimeMinute = 0; // Opening Time in Minute
    const closingHour = 24; // Closing Time in Hour
    const closingMinute = 0; // Closing Time in Minute

    // Check if the current time is within the defined time range
    const isWithinTimeRange = (
        (currentHour > openingTimeHour || (currentHour === openingTimeHour && currentMinute >= openingTimeMinute)) &&
        (currentHour < closingHour || (currentHour === closingHour && currentMinute <= closingMinute))
    );

    return isWithinTimeRange;
}

// Function to add an item to the cart
function addToCart(id) {
    if (isOrderAllowed()) {
        const existingCartItem = cartList.find(item => item.id === id);

        if (existingCartItem) {
            // If the item is already in the cart, increase its quantity by 1
            existingCartItem.quantity += 1;
        } else {
            // If the item is not in the cart, add it to the cart with a quantity of 1
            cartList.push({ ...data[id], quantity: 1 });
        }

        addItem();
        updateCartCount();

        // Show an alert for item added to the cart
        showAlert('Item added to your cart');
    } else {
        // Display a message indicating that orders are not allowed at this time
        showAlert('We are closed. Opening Hour is 6:00 AM to 11:00 PM Ghana Time.');
    }
}








