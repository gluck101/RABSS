function addItem() {
    totalAmount = 0;
    totalItems = 0;
    totalSaving = 0;
    var clrNode = document.getElementById('item-body');
    clrNode.innerHTML = '';

    cartList.forEach((cart) => {
        totalAmount += cart.price * cart.quantity; // Update totalAmount based on quantity
        totalSaving += cart.save * cart.quantity; // Update totalSaving based on quantity
        totalItems += cart.quantity;

        var tempCart = document.createElement('div');
        tempCart.setAttribute('class', 'cart-list');
        tempCart.setAttribute('id', cart.id);

        var quantityDisplay = document.createElement('h3');
        quantityDisplay.setAttribute('class', 'quantity');
        quantityDisplay.innerHTML = 'Quantity: ' + cart.quantity;
        tempCart.appendChild(quantityDisplay);

        // + button to adjust quantity
        var plusButton = document.createElement('button');
        plusButton.innerHTML = '+';
        plusButton.onclick = function () {
            cart.quantity += 1;
            addItem(); // Re-render the cart to reflect quantity change
        };
        tempCart.appendChild(plusButton);

        // - button to adjust quantity
        var minusButton = document.createElement('button');
        minusButton.innerHTML = '-';
        minusButton.onclick = function () {
            if (cart.quantity > 1) {
                cart.quantity -= 1;
                addItem(); // Re-render the cart to reflect quantity change
            }
        };
        tempCart.appendChild(minusButton);
    });

    updateCartCount();
}

